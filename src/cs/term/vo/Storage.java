package cs.term.vo;

public class Storage {
	private String stid;
	private String stmem;
	private String stterm;
	private String stdate;
	
	public Storage() {
		super();
	}

	public String getStid() {
		return stid;
	}

	public void setStid(String stid) {
		this.stid = stid;
	}

	public String getStmem() {
		return stmem;
	}

	public void setStmem(String stmem) {
		this.stmem = stmem;
	}

	public String getStterm() {
		return stterm;
	}

	public void setStterm(String stterm) {
		this.stterm = stterm;
	}

	public String getStdate() {
		return stdate;
	}

	public void setStdate(String stdate) {
		this.stdate = stdate;
	}
	
	
}
